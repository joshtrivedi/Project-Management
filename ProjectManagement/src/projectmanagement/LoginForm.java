    private void jButton_LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LogInActionPerformed
    try{
        if(jTextField_Username.getText().equals("")){
            lbl_U.setVisible(true);
        }if(String.valueOf(jPasswordField1.getPassword()).equals("")){
            lbl_p.setVisible(true);
        }
        else {
            Connection con = MyConnection.getConnection();
            PreparedStatement ps;
            
            try {
                ps = con.prepareStatement("SELECT * FROM user WHERE username = ? AND password = ?");
                        ps.setString(1,jTextField_Username.getText());
                        ps.setString(2,String.valueOf(jPasswordField1.getPassword()));
                        
                        ResultSet rs = ps.executeQuery();
                        if(rs.next()){
                            System.out.println("YES");
                            MainForm mf = new MainForm();
                            mf.setVisible(true);
                            mf.pack();
                            mf.setLocationRelativeTo(null);
                            MainForm.jLabel_username.setText("Welcome "+jTextField_Username.getText());
                            
                            MainForm.jLabel_Stdcount2.setText(" Student Count = "+Integer.toString(MyFunction.countData("student")));
                            
                            MainForm.jLabel_project.setText(" Faculty Count = "+Integer.toString(MyFunction.countData("user")));
                            
                            MainForm.jLabel_projectcnt.setText(" Project Count = "+Integer.toString(MyFunction.countData("project")));
                            this.dispose();
                            
                        }else{
                            System.out.println("NO");
                            JOptionPane.showMessageDialog(null, "Wrong Credentials");
                        }
                        } catch (SQLException ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }catch(NullPointerException e1){
        JOptionPane.showMessageDialog(null, "SQL Database Not Connected");
    }
    }//GEN-LAST:event_jButton_LogInActionPerformed

